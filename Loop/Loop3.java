class Loop3 {
    public static void main(String[] args) {
        int i=3,n1=1;
        while (i>=n1) {
            System.out.println(i+" ");
            i--;
        }
        System.out.println("\n------------------------");

        int j=1,n2=3;
        while (j<=n2) {
            System.out.println(j+" ");
            j++;
        }

    }
}
