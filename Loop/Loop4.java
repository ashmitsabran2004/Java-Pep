class Loop4{
    public static void main(String[]args){
        System.out.println("Spotify - Playlist");
        int i=1, n=3;
        while(i<=n){
            if(i==1)
                System.out.println("1.Ek Dhun");
            else if(i==2)
                System.out.println("1.Barbaad");
            else if(i==3)
                System.out.println("1.Saiyaara");

            i++;
        }

        System.out.println("Playlist is done");
    }
}
