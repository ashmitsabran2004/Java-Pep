class Loop5{
    public static void main(String[]args){



        int i;
        for( i=1; i<=3 ; i++)
            System.out.println(i+" ");
        System.out.println("\n--------------------------------");
        int j=3;
        for(j ; j>=1 ; j--)
            System.out.println(j+" ");
        System.out.println("\n--------------------------------");
        for(int k=1; k<=3; )
            System.out.println(k+" ");
        k+=1;
        break;

    }
}
